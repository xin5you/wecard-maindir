package com.cn.thinkx.oms.module.statement.model;

public class CustomerInfo {
	private String attentionCount;
	private String openAccountCount;
	private String openCardCount;
	public String getAttentionCount() {
		return attentionCount;
	}
	public void setAttentionCount(String attentionCount) {
		this.attentionCount = attentionCount;
	}
	public String getOpenAccountCount() {
		return openAccountCount;
	}
	public void setOpenAccountCount(String openAccountCount) {
		this.openAccountCount = openAccountCount;
	}
	public String getOpenCardCount() {
		return openCardCount;
	}
	public void setOpenCardCount(String openCardCount) {
		this.openCardCount = openCardCount;
	}
	
}
